package com.cg.entity;

public class Student {

	public String name;
	public int id;
	public String dept;
	public String university;

	public Student() {
		
	}
	public Student(String name, int id, String dept, String university) {
		super();
		this.name = name;
		this.id = id;
		this.dept = dept;
		this.university = university;
	}

}
  